package day02wrapperclasses;

public class Wrapper01 {

	public static void main(String[] args) {
		
		//int data tipinin maksimum ve miniimum degerlerini bulup ekrana yazdiriniz
		// int in wrapper classi integer 
	     System.out.println(Integer.MAX_VALUE);// bir classin icindeki metotlara  ulasmak icin class ismindenn sonra nokta koy listeden secim yap
         
	     System.out.println(Integer.MIN_VALUE);
	     
	     // long data tpiin max ve mi degerlerni bulup ekrana yaziniz
	     // wrapper in manasi==> cevreleyen, kusatan gibi. bayaram seeri kabi gibi
	     System.out.println(Long.MAX_VALUE);
	     System.out.println(Long.MIN_VALUE);
	     
	     
	     // tum primitevelrin max ve min degerlerinin wrapper kullanarak ekrana yazdiriniz
	     
	     
	     System.out.println(Character.MAX_VALUE);
	     System.out.println(Character.MIN_VALUE);
	     
	     System.out.println(Short.MAX_VALUE);
	     System.out.println(Short.MIN_VALUE);
	     
	     
	     System.out.println(Float.MAX_VALUE);
	     System.out.println(Float.MIN_VALUE);
	     
	     System.out.println(Byte.MAX_VALUE);
	     System.out.println( Byte.MIN_VALUE);
	     
	     
	     System.out.println(Double.MAX_VALUE);
	     System.out.println(Double.MIN_VALUE);
	     
	     System.out.println(Boolean.TRUE);
	     System.out.println(Boolean.FALSE);
	     
	     
	     
	     
	     
	     
	     
	     
	     
	     
	}

}
