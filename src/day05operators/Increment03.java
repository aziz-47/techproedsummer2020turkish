package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		// int data tipinde 2 tane veriable olusturun
		//bu verableleri en kisa yolu kullnarak 1 artirin.
		// Not=en kisa yol sadece 1 artirmak icin kullaniirlir
		
		
		int sayi1=12;
		sayi1++;// veriable isinden sonra iki kere + sembolu yazarsanzi
		        // o veriablenin degerini 1 artirmsi olursunuz
		
		
		System.out.println(sayi1);//13
		
		int sayi2= 20;
		sayi2++;
		System.out.println(sayi2);//21
		
		

	}

}
